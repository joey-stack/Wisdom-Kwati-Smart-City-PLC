async function seed() {
  console.log(`\nSeeding ${newBlogs.length} new dummy blogs to Firestore...\n`);

  for (const blog of newBlogs) {
    const { slug, ...data } = blog;
    try {
      await setDoc(doc(db, 'blogs', slug), {
        ...data,
        slug,
        createdAt: new Date().toISOString()
      }, { merge: true });
      console.log(`  ✅ ${slug} — "${data.title}"`);
    } catch (err) {
      console.error(`  ❌ ${slug} — Failed: ${err.message}`);
    }
  }

  console.log('\n✅ All 10 dummy blogs seeded successfully!\n');
  process.exit(0);
}

seed().catch((err) => {
  console.error('Fatal seed error:', err);
  process.exit(1);
});
